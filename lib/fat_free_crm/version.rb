module FatFreeCRM
  class Version
    RELEASE = 0
    MAJOR   = 10
    MINOR   = 1

    def self.to_a
      [ RELEASE, MAJOR, MINOR ]
    end
    
    def self.to_s
      self.to_a.join(".")
    end
  end
end